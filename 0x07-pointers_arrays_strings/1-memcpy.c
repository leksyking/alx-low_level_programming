/**
 * _memcpy - a function that copies memory area
 *
 * @dest: memory area
 * @src: source memory area
 * @n: number of bytes
 *
 * Return: a pointer to dest
 */
char *_memcpy(char *dest, char *src, unsigned int n);
{
	unsigned int i, j, k;

	for (i = 0; dest[i] != '\0'; i++)
	{
	}
	for (j = i, k = 0; k < n; j++. k++)
	{
		dest[j] = src[k];
	}
	return (dest);
}
